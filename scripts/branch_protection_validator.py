def validate_security_features(  # noqa: C901
    owner: str, repo: str, token: str | None
) -> dict[str, Any]:
    results: dict[str, Any] = {
        "dependency_review": "unknown",
        "codeql": "unknown",
        _SECRET_SCANNING: "unknown",
        "push_protection": "unknown",
        "dependabot_alerts": "unknown",
        "dependabot_security_updates": "unknown",
        "sbom": "unknown",
        "artifact_attestations": "unknown",
        "oidc": "unknown",
        "warnings": [],
        "errors": [],
    }
    dep_review_url = (
        f"https://api.github.com/repos/{owner}/{repo}/dependency-graph/snapshots"
    )
    status, _ = api_get(dep_review_url, token)
    if status == 200:
        results["dependency_review"] = "available"
    elif status == 403:
        results["dependency_review"] = "unverifiable"
        results["warnings"].append(
            "Insufficient token permissions to read dependency graph"
        )
    else:
        results["dependency_review"] = "unavailable"

    codeql_url = f"https://api.github.com/repos/{owner}/{repo}/code-scanning/setup"
    status, data = api_get(codeql_url, token)
    if status == 200 and isinstance(data, dict):
        results["codeql"] = "enabled"
    elif status == 404:
        results["codeql"] = "disabled"
    elif status == 403:
        results["codeql"] = "unverifiable"
        results["warnings"].append(
            "Insufficient token permissions to read CodeQL setup"
        )
    else:
        results["codeql"] = "unknown"

    ss_url = f"https://api.github.com/repos/{owner}/{repo}/secret-scanning"
    status, data = api_get(ss_url, token)
    if status == 200 and isinstance(data, dict):
        results[_SECRET_SCANNING] = "enabled"
        results["push_protection"] = (
            "enabled" if data.get("push_protection_enabled") else "disabled"
        )
    elif status == 404:
        results[_SECRET_SCANNING] = "disabled"
        results["push_protection"] = "disabled"
    elif status == 403:
        results[_SECRET_SCANNING] = "unverifiable"
        results["push_protection"] = "unverifiable"
        results["warnings"].append(
            "Insufficient token permissions to read secret scanning"
        )
    else:
        results[_SECRET_SCANNING] = "unknown"
        results["push_protection"] = "unknown"
        results["warnings"].append(f"Secret Scanning API returned HTTP {status}")

    da_url = f"https://api.github.com/repos/{owner}/{repo}/dependabot/alerts"
    status, _ = api_get(da_url, token)
    if status == 200:
        results["dependabot_alerts"] = "enabled"
    elif status == 403:
        results["dependabot_alerts"] = "unverifiable"
        results["warnings"].append(
            "Insufficient token permissions to read Dependabot alerts"
        )
    else:
        results["dependabot_alerts"] = "disabled_or_unavailable"

    dsu_url = f"https://api.github.com/repos/{owner}/{repo}/dependabot/updates"
    status, data = api_get(dsu_url, token)
    if status == 200 and isinstance(data, list):
        results["dependabot_security_updates"] = "enabled"
    elif status == 403:
        results["dependabot_security_updates"] = "unverifiable"
    else:
        results["dependabot_security_updates"] = "disabled_or_unavailable"

    sbom_url = f"https://api.github.com/repos/{owner}/{repo}/sbom"
    status, _ = api_get(sbom_url, token)
    if status == 200:
        results["sbom"] = "available"
    elif status == 403:
        results["sbom"] = "unverifiable"
    else:
        results["sbom"] = "unavailable"

    att_url = f"https://api.github.com/repos/{owner}/{repo}/attestations"
    status, _ = api_get(att_url, token)
    if status == 200:
        results["artifact_attestations"] = "available"
    elif status == 403:
        results["artifact_attestations"] = "unverifiable"
    else:
        results["artifact_attestations"] = "unavailable"

    oidc_url = f"https://api.github.com/repos/{owner}/{repo}/actions/oidc"
    status, _ = api_get(oidc_url, token)
    if status == 200:
        results["oidc"] = "available"
    elif status == 403:
        results["oidc"] = "unverifiable"
    else:
        results["oidc"] = "unavailable"

    return results


def build_report(
    owner: str,
    repo: str,
    default_protection: dict[str, Any],
    other_protections: dict[str, dict[str, Any]],
    repo_info: dict[str, Any],
    rulesets: dict[str, Any],
    workflow_integrity: dict[str, Any],
    security_features: dict[str, Any],
    workflow_security: dict[str, Any],
    required_checks: list[str],
    execution_duration: float | None = None,
) -> str:
    lines: list[str] = [REPORT_HEADER, ""]
    lines.append(f"Repository: `{owner}/{repo}`")
    lines.append(f"Default branch: `{repo_info.get('default_branch') or 'unknown'}`")
    protected = repo_info.get("protected_branches", [])
    if protected:
        lines.append(f"Protected branches: `{', '.join(protected)}`")
    else:
        lines.append("Protected branches: `none detected from repo metadata`")
    lines.append("")
    overall_score = 100
    critical_failures = 0
    warning_count = 0

    section_lines, crit_delta, score_delta, warn_delta = (
        _render_branch_protection_section(
            default_protection=default_protection,
            other_protections=other_protections,
            required_checks=required_checks,
        )
    )
    lines.extend(section_lines)
    critical_failures += crit_delta
    overall_score += score_delta
    warning_count += warn_delta

    section_lines, warn_delta = _render_merge_queue_section(
        default_protection=default_protection,
    )
    lines.extend(section_lines)
    warning_count += warn_delta

    section_lines, warn_delta = _render_rulesets_section(rulesets=rulesets)
    lines.extend(section_lines)
    warning_count += warn_delta

    section_lines, warn_delta = _render_security_features_section(
        security_features=security_features,
    )
    lines.extend(section_lines)
    warning_count += warn_delta

    section_lines, warn_delta = _render_workflow_security_section(
        workflow_security=workflow_security,
    )
    lines.extend(section_lines)
    warning_count += warn_delta

    lines.extend(_render_permissions_audit(repo_info=repo_info))

    section_lines, crit_delta, score_delta = _render_workflow_integrity_section(
        workflow_integrity=workflow_integrity,
    )
    lines.extend(section_lines)
    critical_failures += crit_delta
    overall_score += score_delta

    if execution_duration is not None:
        lines.append("## Execution Metrics")
        lines.append(f"- Duration: {execution_duration:.2f}s")
        remaining = _rate_limit_cache.get("remaining") or "unknown"
        observed_at = _rate_limit_cache.get("observed_at") or "unknown"
        lines.append(f"- Rate limit remaining: {remaining}")
        lines.append(f"- Rate limit observed at: {observed_at}")
        lines.append("")

    lines.extend(
        _render_overall_section(
            overall_score=overall_score,
            critical_failures=critical_failures,
            warning_count=warning_count,
        )
    )
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description="Branch Protection Validator")
    parser.add_argument("--json", action="store_true", help="Output report as JSON")
    parser.add_argument("--token", help="GitHub token override")
    parser.add_argument("--repo", help="Repository override (owner/repo)")
    parser.add_argument(
        "--required-checks", help="Comma-separated required check names"
    )
    parser.add_argument(
        "--output-dir",
        default=".",
        help="Directory to write report artifacts",
    )
    args = parser.parse_args()

    token = args.token or get_token()
    if not token:
        print("SKIPPED: GITHUB_TOKEN or GH_TOKEN is not available.")
        return 0

    owner, repo = None, None
    if args.repo and "/" in args.repo:
        owner, repo = args.repo.split("/", 1)
    else:
        owner, repo = get_repo_inputs()

    if not owner or not repo:
        print("SKIPPED: Repository could not be determined.")
        return 0

    required_checks = DEFAULT_REQUIRED_CHECKS
    if args.required_checks:
        required_checks = [
            c.strip() for c in args.required_checks.split(",") if c.strip()
        ]

    started_at = time.time()
    repo_info = validate_repo_info(owner, repo, token)
    default_branch = repo_info.get("default_branch")
    protected_branches = repo_info.get("protected_branches", [])

    default_protection: dict[str, Any] = {"status": "unknown", "exists": False}
    if default_branch:
        default_protection = validate_branch_protection(
            owner, repo, default_branch, token
        )

    other_protections: dict[str, dict[str, Any]] = {}
    for branch in protected_branches:
        if branch == default_branch:
            continue
        other_protections[branch] = validate_branch_protection(
            owner, repo, branch, token
        )

    rulesets = validate_rulesets(owner, repo, token)
    workflow_integrity = validate_workflow_integrity(owner, repo, token)
    security_features = validate_security_features(owner, repo, token)
    workflow_security = validate_workflow_security(owner, repo, token)
    execution_duration = time.time() - started_at

    if args.json:
        payload = _build_json_payload(
            owner=owner,
            repo=repo,
            token=token,
            default_branch=default_branch,
            default_protection=default_protection,
            other_protections=other_protections,
            repo_info=repo_info,
            rulesets=rulesets,
            workflow_integrity=workflow_integrity,
            security_features=security_features,
            workflow_security=workflow_security,
            execution_duration=execution_duration,
        )
        print(json.dumps(payload, indent=2, default=str))
        return 0

    report = build_report(
        owner=owner,
        repo=repo,
        default_protection=default_protection,
        other_protections=other_protections,
        repo_info=repo_info,
        rulesets=rulesets,
        workflow_integrity=workflow_integrity,
        security_features=security_features,
        workflow_security=workflow_security,
        required_checks=required_checks,
        execution_duration=execution_duration,
    )
    print(report)

    _write_reports(
        args.output_dir,
        owner,
        repo,
        token,
        default_branch,
        default_protection,
        other_protections,
        repo_info,
        rulesets,
        workflow_integrity,
        security_features,
        workflow_security,
        execution_duration,
        report,
    )

    critical_failures = _count_critical_failures(
        default_protection, other_protections, workflow_integrity
    )
    return 1 if critical_failures > 0 else 0


def _build_json_payload(
    owner: str,
    repo: str,
    token: str,
    default_branch: str | None,
    default_protection: dict[str, Any],
    other_protections: dict[str, dict[str, Any]],
    repo_info: dict[str, Any],
    rulesets: dict[str, Any],
    workflow_integrity: dict[str, Any],
    security_features: dict[str, Any],
    workflow_security: dict[str, Any],
    execution_duration: float,
) -> dict[str, Any]:
    summary_parts = []
    for protection in [default_protection, *other_protections.values()]:
        summary_parts.extend(protection.get("errors", []))
    summary_parts.extend(workflow_integrity.get("errors", []))
    critical_failures = sum(
        1
        for message in summary_parts
        if "not enabled" in message or "Missing" in message
    )
    warning_count = sum(
        1
        for protection in [default_protection, *other_protections.values()]
        for message in protection.get("warnings", [])
    )
    warning_count += len(rulesets.get("warnings", []))
    warning_count += len(security_features.get("warnings", []))
    warning_count += len(workflow_security.get("warnings", []))
    status = "FAIL" if critical_failures > 0 else "PASS"
    return {
        "version": VALIDATOR_VERSION,
        "timestamp": datetime.now(UTC).isoformat(),
        "repository": f"{owner}/{repo}",
        "default_branch": default_branch,
        "score": max(
            0,
            100
            + sum(
                protection.get("score_delta", 0)
                for protection in [default_protection, *other_protections.values()]
            ),
        ),
        "status": status,
        "execution_duration_seconds": round(execution_duration, 3),
        "token": {
            "type": "GITHUB_TOKEN" if not os.environ.get("GH_TOKEN") else "GH_TOKEN",
            "masked": _mask_token(token),
            "permissions_visible": list(repo_info.get("permissions", {}).keys()),
        },
        "rate_limit": {
            key: value for key, value in _rate_limit_cache.items() if value is not None
        },
        "validations": {
            "branch_protection": {
                "default": default_protection,
                "other": other_protections,
            },
            "rulesets": {
                "count": rulesets.get("count", 0),
                "status": rulesets.get("status", "unknown"),
                "items": rulesets.get("rulesets", []),
            },
            "security_features": {
                key: value
                for key, value in security_features.items()
                if key not in {"warnings", "errors"}
            },
            "workflow_security": workflow_security.get("findings", []),
            "workflow_integrity": {
                "missing": workflow_integrity.get("missing", []),
                "errors": workflow_integrity.get("errors", []),
            },
        },
        "summary": {
            "critical_failures": critical_failures,
            "warning_count": warning_count,
            "execution_duration_seconds": round(execution_duration, 3),
        },
        "api_endpoint_states": {
            "repo_info": "ok" if repo_info.get("default_branch") else "unverifiable",
            "rulesets": rulesets.get("status", "unknown"),
            "security_features": {
                key: value
                for key, value in security_features.items()
                if key not in {"warnings", "errors"}
            },
        },
    }


def _write_reports(
    output_dir: str,
    owner: str,
    repo: str,
    token: str,
    default_branch: str | None,
    default_protection: dict[str, Any],
    other_protections: dict[str, dict[str, Any]],
    repo_info: dict[str, Any],
    rulesets: dict[str, Any],
    workflow_integrity: dict[str, Any],
    security_features: dict[str, Any],
    workflow_security: dict[str, Any],
    execution_duration: float,
    report: str,
) -> None:
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    json_payload = _build_json_payload(
        owner=owner,
        repo=repo,
        token=token,
        default_branch=default_branch,
        default_protection=default_protection,
        other_protections=other_protections,
        repo_info=repo_info,
        rulesets=rulesets,
        workflow_integrity=workflow_integrity,
        security_features=security_features,
        workflow_security=workflow_security,
        execution_duration=execution_duration,
    )
    report_text = json.dumps(json_payload, indent=2, default=str)
    _atomic_write(out / JSON_REPORT_NAME, report_text)
    _atomic_write(out / REPORT_NAME, report)


def _atomic_write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp = tempfile.mkstemp(dir=str(path.parent), prefix=".tmp-", suffix=path.suffix)
    try:
        with os.fdopen(fd, "w", encoding="utf-8") as fh:
            fh.write(content)
        os.replace(tmp, str(path))
    except Exception:
        try:
            os.unlink(tmp)
        except OSError:
            pass
        raise


def _count_critical_failures(
    default_protection: dict[str, Any],
    other_protections: dict[str, dict[str, Any]],
    workflow_integrity: dict[str, Any],
) -> int:
    failures = 0
    for protection in [default_protection, *other_protections.values()]:
        if protection.get("status") == "unverifiable":
            continue
        failures += sum(
            1
            for e in protection.get("errors", [])
            if "not enabled" in e or "Missing" in e
        )
    failures += len(workflow_integrity.get("errors", []))
    return failures


if __name__ == "__main__":
    sys.exit(main())
