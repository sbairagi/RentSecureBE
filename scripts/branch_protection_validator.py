def _mask_token(token: str) -> str:
    if not token:
        return ""
    if len(token) <= 8:
        return "***"
    return f"{token[:4]}...{token[-4:]}"


def _create_session() -> Any:
    session = requests.Session()
    retry_strategy = Retry(
        total=MAX_RETRIES,
        backoff_factor=RETRY_BACKOFF_FACTOR,
        status_forcelist=[429, 500, 502, 503, 504],
        allowed_methods=["GET"],
    )
    adapter = HTTPAdapter(max_retries=retry_strategy)
    session.mount("https://", adapter)
    session.mount("http://", adapter)
    return session


_session = _create_session()


def _get_rate_limit_info(response: Any) -> dict[str, Any]:
    headers = getattr(response, "headers", {}) or {}
    remaining = headers.get("X-RateLimit-Remaining") or headers.get(
        "x-ratelimit-remaining"
    )
    reset = headers.get("X-RateLimit-Reset") or headers.get("x-ratelimit-reset")
    observed_at = datetime.now(UTC).isoformat()
    return {
        "remaining": remaining,
        "reset": reset,
        "observed_at": observed_at,
    }


def _respect_rate_limit(response: Any) -> None:
    global _rate_limit_cache
    info = _get_rate_limit_info(response)
    _rate_limit_cache = info
    if info.get("remaining") == "0" and info.get("reset"):
        try:
            reset_ts = int(info["reset"])
            now = time.time()
            wait = max(0, reset_ts - now) + 1
            if 0 < wait < 60:
                time.sleep(wait)
        except (ValueError, TypeError):
            pass


def api_get(
    url: str, token: str | None, params: dict[str, Any] | None = None
) -> tuple[int, dict[str, Any] | list[Any] | None]:
    headers = {
        "Accept": "application/vnd.github+json",
        "X-GitHub-Api-Version": GITHUB_API_VERSION,
    }
    if token:
        headers["Authorization"] = f"Bearer {token}"
    try:
        response = _session.get(
            url,
            headers=headers,
            params=params,
            timeout=REQUEST_TIMEOUT,
        )
        _respect_rate_limit(response)
        try:
            data = response.json()
        except ValueError:
            data = {"_error": "Invalid JSON response"}
        return response.status_code, data
    except Exception as exc:  # noqa: BLE001
        return 0, {"_error": str(exc)}


def extract_check_contexts(checks: list[dict[str, Any]]) -> set[str]:
    contexts: set[str] = set()
    for check in checks:
        context = check.get("context")
        if context:
            contexts.add(context)
    return contexts


def get_token() -> str | None:
    gh_token = os.environ.get("GH_TOKEN") or os.environ.get("GITHUB_TOKEN")
    if gh_token and gh_token.strip():
        return gh_token.strip()
    return None


def get_repo_inputs() -> tuple[str | None, str | None]:
    repo_input = os.environ.get("GITHUB_REPOSITORY")
    if repo_input and "/" in repo_input:
        parts = repo_input.split("/", 1)
        if len(parts) == 2 and parts[0] and parts[1]:
            return parts[0], parts[1]
    return None, None


def classify_protection_status(
    status: int, data: dict[str, Any] | list[Any] | None
) -> tuple[str, str | None]:
    """Classify protection API response into actionable states."""
    if status == 404:
        return "disabled", None
    if status == 403:
        return (
            "unverifiable",
            "Insufficient token permissions to read branch protection",
        )
    if status != 200:
        err = data.get("_error") if isinstance(data, dict) else "unknown error"
        return "unverifiable", f"HTTP {status}: {err}"
    return "enabled", None


def validate_repo_info(owner: str, repo: str, token: str | None) -> dict[str, Any]:
    url = f"https://api.github.com/repos/{owner}/{repo}"
    status, data = api_get(url, token)
    results: dict[str, Any] = {
        "default_branch": None,
        "protected_branches": [],
        "permissions": {},
        "warnings": [],
        "errors": [],
    }
    if status != 200 or not isinstance(data, dict):
        err = data.get("_error") if isinstance(data, dict) else "unknown error"
        results["warnings"].append(f"Could not fetch repo info: HTTP {status} - {err}")
        return results

    results["default_branch"] = data.get("default_branch")
    protection = data.get("branch_protection")
    if isinstance(protection, list):
        results["protected_branches"] = [
            b.get("name") for b in protection if isinstance(b, dict) and b.get("name")
        ]
    permissions = data.get("permissions")
    if isinstance(permissions, dict):
        results["permissions"] = permissions
    return results


def validate_branch_protection(
    owner: str, repo: str, branch: str, token: str | None
) -> dict[str, Any]:
    url = f"https://api.github.com/repos/{owner}/{repo}/branches/{branch}/protection"
    status, data = api_get(url, token)
    results: dict[str, Any] = {
        "branch": branch,
        "status": "unknown",
        "exists": False,
        "checks": {},
        "warnings": [],
        "errors": [],
        "http_status": status,
    }
    protection_state, note = classify_protection_status(status, data)
    results["status"] = protection_state
    if note:
        results["warnings"].append(note)
        return results

    results["exists"] = True
    required_status_checks = (
        data.get("required_status_checks", {}) if isinstance(data, dict) else {}
    )
    checks = (
        required_status_checks.get("checks", [])
        if isinstance(required_status_checks, dict)
        else []
    )
    results["checks"]["contexts"] = sorted(extract_check_contexts(checks))
    results["checks"]["strict"] = (
        required_status_checks.get("strict", False)
        if isinstance(required_status_checks, dict)
        else False
    )
    results["checks"]["required_approving_review_count"] = None
    results["checks"]["dismiss_stale_reviews"] = False
    results["checks"]["require_code_owner_reviews"] = False
    results["checks"]["require_last_push_approval"] = False
    enforce_admins = data.get("enforce_admins", {}) if isinstance(data, dict) else {}
    results["checks"]["enforce_admins"] = (
        enforce_admins.get("enabled", False)
        if isinstance(enforce_admins, dict)
        else False
    )
    allow_force_pushes = (
        data.get("allow_force_pushes", {}) if isinstance(data, dict) else {}
    )
    results["checks"]["allow_force_pushes"] = (
        allow_force_pushes.get("enabled", False)
        if isinstance(allow_force_pushes, dict)
        else False
    )
    allow_deletions = data.get("allow_deletions", {}) if isinstance(data, dict) else {}
    results["checks"]["allow_deletions"] = (
        allow_deletions.get("enabled", False)
        if isinstance(allow_deletions, dict)
        else False
    )
    required_pull_request_reviews = (
        data.get("required_pull_request_reviews", {}) if isinstance(data, dict) else {}
    )
    if isinstance(required_pull_request_reviews, dict):
        results["checks"]["required_approving_review_count"] = (
            required_pull_request_reviews.get("required_approving_review_count")
        )
        results["checks"]["dismiss_stale_reviews"] = required_pull_request_reviews.get(
            "dismiss_stale_reviews", False
        )
        results["checks"]["require_code_owner_reviews"] = (
            required_pull_request_reviews.get("require_code_owner_reviews", False)
        )
        results["checks"]["require_last_push_approval"] = (
            required_pull_request_reviews.get("require_last_push_approval", False)
        )
    required_linear_history = (
        data.get("required_linear_history", {}) if isinstance(data, dict) else {}
    )
    results["checks"]["required_linear_history"] = (
        required_linear_history.get("enabled", False)
        if isinstance(required_linear_history, dict)
        else False
    )
    required_conversation_resolution = (
        data.get("required_conversation_resolution", {})
        if isinstance(data, dict)
        else {}
    )
    results["checks"]["required_conversation_resolution"] = (
        required_conversation_resolution.get("enabled", False)
        if isinstance(required_conversation_resolution, dict)
        else False
    )
    commit_signing_required = (
        data.get("commit_signing_required", {}) if isinstance(data, dict) else {}
    )
