    lines: list[str] = ["## Security Features"]
    warning_delta = 0
    for feature, value in security_features.items():
        if feature in {"warnings", "errors"}:
            continue
        display = value.replace("_", " ").title()
        if value in {"enabled", "available"}:
            lines.append(f"- {display}: ✅ {value.title()}")
        elif value in {"disabled"}:
            lines.append(f"- {display}: ⚠️ Disabled")
            warning_delta += 1
        elif value in {"unverifiable"}:
            reason = security_features.get("warnings", [""])[0]
            lines.append(f"- {display}: ⚠️ Unable to verify — {reason}")
            warning_delta += 1
        else:
            lines.append(f"- {display}: ⚠️ Unable to verify")
            warning_delta += 1
    lines.append("")
    return lines, warning_delta


