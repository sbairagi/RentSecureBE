def _render_single_branch_protection(  # noqa: C901
    branch_name: str,
    protection: dict[str, Any],
    required_checks: list[str],
) -> tuple[list[str], int, int, int]:
    lines: list[str] = []
    critical_delta = 0
    score_delta = 0
    warning_delta = 0
    protection_state = protection.get("status", "unknown")

    if protection_state == "unverifiable":
        reason = protection.get("warnings", ["unknown"])[0]
        lines.append(f"- **{branch_name}**: ⚠️ Unable to verify — {reason}")
        warning_delta += 1
        return lines, critical_delta, score_delta, warning_delta

    if not protection.get("exists"):
        lines.append(f"- **{branch_name}**: ❌ FAIL — Branch protection is not enabled")
        critical_delta += 1
        score_delta -= 25
        return lines, critical_delta, score_delta, warning_delta

    lines.append(f"- **{branch_name}**: ✅ Enabled")
    contexts = protection.get("checks", {}).get("contexts", [])
    missing = [c for c in required_checks if c not in contexts]
    if missing:
        lines.append(f"  - Required checks: ❌ Missing: {', '.join(missing)}")
        critical_delta += 1
        score_delta -= 15
    else:
        lines.append(
            f"  - Required checks: ✅ All {len(required_checks)} checks present"
        )
    checks = protection.get("checks", {})
    if checks.get("allow_force_pushes"):
        lines.append("  - Force push: ❌ FAIL — Force push is enabled")
        critical_delta += 1
        score_delta -= 15
    else:
        lines.append("  - Force push: ✅ Disabled")
    if checks.get("allow_deletions"):
        lines.append("  - Branch deletion: ❌ FAIL — Branch deletion is enabled")
        critical_delta += 1
        score_delta -= 15
    else:
        lines.append("  - Branch deletion: ✅ Disabled")
    if checks.get("enforce_admins"):
        lines.append("  - Admin enforcement: ✅ Admins must obey rules")
    else:
        lines.append("  - Admin enforcement: ❌ FAIL — Admins can bypass")
        critical_delta += 1
        score_delta -= 10
    if checks.get("required_conversation_resolution"):
        lines.append("  - Conversation resolution: ✅ Required")
    else:
        lines.append("  - Conversation resolution: ⚠️ Not required")
        warning_delta += 1
    approve_count = checks.get("required_approving_review_count")
    if approve_count and approve_count >= REQUIRED_REVIEWS_MIN:
        lines.append(f"  - Required reviews: ✅ {approve_count} approvals")
    else:
        actual = approve_count if approve_count is not None else 0
        lines.append(
            f"  - Required reviews: ❌ FAIL — {actual} approvals "
            f"(expected >= {REQUIRED_REVIEWS_MIN})"
        )
        critical_delta += 1
        score_delta -= 10
    if checks.get("dismiss_stale_reviews"):
        lines.append("  - Dismiss stale reviews: ✅ Enabled")
    else:
        lines.append("  - Dismiss stale reviews: ⚠️ Disabled")
        warning_delta += 1
    if checks.get("require_code_owner_reviews"):
        lines.append("  - Code owner reviews: ✅ Required")
    else:
        lines.append("  - Code owner reviews: ⚠️ Not required")
        warning_delta += 1
    if checks.get("required_linear_history"):
        lines.append("  - Linear history: ✅ Required")
    else:
        lines.append("  - Linear history: ⚠️ Not required")
        warning_delta += 1
    allowed = checks.get("allowed_merge_methods", [])
    if allowed:
        lines.append(f"  - Merge methods: {', '.join(allowed)}")
    else:
        lines.append("  - Merge methods: ⚠️ Not configured")
    return lines, critical_delta, score_delta, warning_delta


def _render_branch_protection_section(
    default_protection: dict[str, Any],
    other_protections: dict[str, dict[str, Any]],
    required_checks: list[str],
) -> tuple[list[str], int, int, int]:
    lines: list[str] = ["## Branch Protection"]
    critical_delta = 0
    score_delta = 0
    warning_delta = 0

    for branch_name, protection in [("default", default_protection)] + list(
        other_protections.items()
    ):
        branch_lines, crit, score, warn = _render_single_branch_protection(
            branch_name=branch_name,
            protection=protection,
            required_checks=required_checks,
        )
        lines.extend(branch_lines)
        critical_delta += crit
        score_delta += score
        warning_delta += warn
    lines.append("")
    return lines, critical_delta, score_delta, warning_delta


